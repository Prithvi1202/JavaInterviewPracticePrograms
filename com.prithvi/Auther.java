class Auther{
	String autherName;
	int age;
	String place;
	Auther(String name, int age, String place){
		this.autherName = name;
		this.age = age;
		this.place = place;
	}

		public String getAutherName(){
			return autherName;
		}
		public int  getAge(){
			return age;
		}
		public String getPlace(){
			return place;
		}
}